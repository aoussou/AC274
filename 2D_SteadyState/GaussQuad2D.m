% GAUSS  nodes x (Legendre points) and weights w
%        for Gauss quadrature
% From Spectral Methods in MATLAB, by Nick Trefethen

  function [x,w] = GaussQuad2D(N)
switch N
    case 1
        x = [1/3,1/3];
        w = 0.5;
    case 2
        x = [2/3,1/6; 1/6 2/3; 1/6 1/6];
        w = 1/6*ones(3,1);
    case 3
        x = [0.1550510257 0.1785587282;...
             0.6449489742 0.0750311102;...
             0.1550510257 0.6663902460;...
             0.6449489742 0.2800199154];
         w = [0.1590206908;0.0909793091;0.1590206908;0.0909793091];
    case 4
        x = [0.4459484909 0.4459484909;...
             0.1081030181 0.4459484909;...
             0.4459484909 0.1081030181;...
             0.0915762135 0.0915762135;...
             0.8168475729 0.0915762135;...
             0.0915762135 0.8168475729];
         w = [0.1116907948*ones(3,1);0.0549758718*ones(3,1)];
end




        